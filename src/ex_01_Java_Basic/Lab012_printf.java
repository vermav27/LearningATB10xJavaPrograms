package ex_01_Java_Basic;

public class Lab012_printf {

    public static void main(String[] args) {

        //print f is used for the formatting
        // "%d" : int, short, byte, long
        // "%f" : float
        // "%b" : boolean
        // "%s" : string
        float x = 10.45f;
        System.out.printf("This is a float number : %f",x);

    }

}
